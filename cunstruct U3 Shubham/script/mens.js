     function displayData(tshirtData){
 
         document.getElementById("innerbox1").innerHTML=""
         
         tshirtData.map(function(el,i){
 
             let div=document.createElement("div")
 
             var image= document.createElement("img")
             image.setAttribute("src",el.imageUrl)
             image.setAttribute("id","pic")
 
             let comp=document.createElement("p")
             comp.innerText=el.comp
             comp.setAttribute("id","compname")
 
             let name=document.createElement("p")
             name.innerText=el.tshirtName
             name.setAttribute("id","tshirtsname")
 
             let Price= document.createElement("p")
             Price.innerText= el.disPrice
             Price.setAttribute("id","price")
 
             var membr= document.createElement("p")
             membr.innerText= el.member
             membr.setAttribute("id", "member")
 
            
 
             div.append(image,comp,name,Price,membr)
 
             div.addEventListener("click",function(){
                 seeInfo(el)
             })
            
 
             document.getElementById("innerbox1").append(div)
 
             
 
         })
     }
 
